<?php

/**
 * Number Calculation class
 */

namespace phpclass\AssignmentFalabella\src;

class NumberCalculation
{
    /**
     * remainderCalculation function
     *
     * @param [int] $number
     * @param [int] $param
     * @return void
     */
    function remainderCalculation($number, $param)
    {
        return $number % $param;
    }

    /**
     * addRemainder function
     *
     * @param [int] $no3
     * @param [int] $no5
     * @return void
     */
    function addRemainder($no3, $no5)
    {
        return $no3 + $no5;
    }
}
